class Main extends StatefulWidget {
  const Main({super.key});

  @override
  State<Main> createState() => _MainState();
}

class _MainState extends State<Main> {
  @override
  Widget build(BuildContext context) {

    AssetImage whatImage(n, i){
      String result = n[i];
      result = result.toString().toLowerCase();
      result+=".png";
      return AssetImage("assets/Icons/$result");
    }

    int howManyDone() {
      int count = 0;
      for (int i = 0; i < achievementDesc["template"].length; i++) {
        if (achievementDesc["template"][i]["finished"]) {
          count++;
        }
      }
      return count;
    }

    void deleteSave() async {
      final directory = await getApplicationDocumentsDirectory();
      final file = File('${directory.path}/progress.json');
      await file.delete();
    }

    void reset() async {
      await showDialog(
        context: context,
        builder: (BuildContext context) {
          return AlertDialog(
            title: const Text("Reset All Achievement Progress?"),
            actions: [
              IconButton(
                onPressed: () {
                  setState(
                    () {
                      deleteSave();
                      for (int i = 0;
                          i < achievementDesc["template"].length;
                          i++) {
                        achievementDesc["template"][i]["finished"] = false;
                      }
                      //saveProgress();
                      Navigator.pop(context);
                    },
                  );
                },
                icon: const Icon(
                  Icons.check,
                  size: 30,
                ),
              ),
              IconButton(
                onPressed: () {
                  setState(
                    () {
                      Navigator.pop(context);
                    },
                  );
                },
                icon: const Icon(
                  Icons.cancel,
                  size: 30,
                ),
              ),
            ],
          );
        },
      );
    }

    int done = howManyDone();
    return Column(
      children: [
        Expanded(
          flex: 2,
          child: Container(
            color: Colors.black,
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                const Padding(
                  padding: EdgeInsets.fromLTRB(10, 48, 0, 10),
                  child: Text(
                    "Achievements",
                    style: TextStyle(fontSize: 36, color: Colors.white),
                  ),
                ),
                Padding(
                  padding:
                      const EdgeInsets.symmetric(horizontal: 10, vertical: 5),
                  child: Row(
                    mainAxisAlignment: MainAxisAlignment.spaceBetween,
                    children: [
                      Text(
                        "Progress: $done / ${achievementDesc["template"].length}",
                        style:
                            const TextStyle(fontSize: 24, color: Colors.white),
                      ),
                      IconButton(
                        onPressed: () {
                          setState(
                            () {
                              reset();
                            },
                          );
                        },
                        icon: const Icon(
                          Icons.restart_alt_outlined,
                          color: Colors.white,
                        ),
                      ),
                      IconButton(
                        onPressed: () {
                          Scaffold.of(context).openEndDrawer();
                        },
                        icon: const Icon(
                          Icons.menu,
                          color: Colors.white,
                        ),
                      )
                    ],
                  ),
                ),
                LinearProgressIndicator(
                  value: done / achievementDesc["template"].length,
                  minHeight: 10,
                  valueColor: const AlwaysStoppedAnimation(Colors.red),
                  backgroundColor: Colors.grey[900],
                ),
              ],
            ),
          ),
        ),
        Expanded(
          flex: 7,
          child: Container(
            height: MediaQuery.of(context).size.height,
            decoration: const BoxDecoration(
                image: DecorationImage(
              image: AssetImage("assets/wallpaper.jpg"),
              fit: BoxFit.fill,
            )),
            child: ListView.builder(
              shrinkWrap: true,
              itemCount: achievementDesc["template"].length,
              itemBuilder: (BuildContext context, int index) {
                return Container(
                  decoration: BoxDecoration(
                    borderRadius: BorderRadius.circular(10),
                    color: Colors.black,
                  ),
                  margin: const EdgeInsets.fromLTRB(10, 0, 15, 15),
                  padding: const EdgeInsets.fromLTRB(10, 15, 10, 0),
                  child: Row(
                    children: [
                      SizedBox(
                        width: MediaQuery.of(context).size.width - 100,
                        child: Column(
                          crossAxisAlignment: CrossAxisAlignment.start,
                          children: [
                            Text(
                              achievementDesc["template"][index]["name"],
                              style: const TextStyle(
                                  color: Colors.white, fontSize: 24),
                            ),
                            Padding(
                              padding: const EdgeInsets.fromLTRB(8, 6, 0, 20),
                              child: Text(
                                achievementDesc["template"][index]["desc"],
                                softWrap: true,
                                style: const TextStyle(
                                    color: Colors.white, fontSize: 18),
                              ),
                            ),
                            Row(
                              children: [
                                for(int i=0;i<achievementDesc["template"][index]["type"].length;i++) //tak, to działa i jestem z tego powodu bardzo zadowolony :3
                                  Padding(
                                  padding: const EdgeInsets.fromLTRB(8, 2, 20, 20),
                                  child: Image(
                                    image: whatImage(achievementDesc["template"][index]["type"], i),
                                    height: 60,
                                  ),
                                ),
                              ],
                            ),
                          ],
                        ),
                      ),
                      Transform.scale(
                        scale: 1.3,
                        child: Checkbox(
                          fillColor: MaterialStateProperty.all(Colors.white),
                          checkColor: Colors.red,
                          materialTapTargetSize: MaterialTapTargetSize.padded,
                          value: achievementDesc["template"][index]["finished"],
                          onChanged: (bool? value) {
                            setState(
                              () {
                                achievementDesc["template"][index]["finished"] =
                                    value;
                                saveProgress();
                              },
                            );
                          },
                        ),
                      ),
                    ],
                  ),
                );
              },
            ),
          ),
        ),
      ],
    );
  }
}
