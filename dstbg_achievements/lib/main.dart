void saveProgress() async {
  final directory = await getApplicationDocumentsDirectory();
  final file = File('${directory.path}/progress.json');
  await file.writeAsString(jsonEncode(achievementDesc));
}

dynamic template;
dynamic achievementDesc;
loadTemplate() async {
  String response;
  response = await rootBundle.loadString("assets/Templates/template2.json");
  template = jsonDecode(response);
  achievementDesc = template;
}

loadProgress() async {
  final directory = await getApplicationDocumentsDirectory();
  if (await File('${directory.path}/progress.json').exists()) {
    File file = File('${directory.path}/progress.json');
    String read = await file.readAsString();
    final result = jsonDecode(read);
    achievementDesc = result;
  } else {
    await loadTemplate();
  }
}

List<String> types = ["attack","block","death","encounter","equipment"];
List<String> availableTypes = List.from(types);
List<bool> filterFinished = [true, true];
class NavDrawer extends StatefulWidget {
  const NavDrawer({super.key});

  @override
  State<NavDrawer> createState() => _NavDrawerState();
}

class _NavDrawerState extends State<NavDrawer> {
  @override
  Widget build(BuildContext context) {
    return Drawer(
      backgroundColor: Colors.black,
      child: ListView(
        children: [
          const DrawerHeader(
            decoration: BoxDecoration(
              color: Colors.black,
            ),
            child: Text("Filter Achievements by type",textAlign: TextAlign.left,style: TextStyle(color: Colors.white,fontSize: 36)),
          ),
          
          Row(
            children:[ 
              const Padding(
                padding: EdgeInsets.fromLTRB(20, 0, 0, 0),
                child: Text("Unfinished", style:TextStyle(color: Colors.white)),
              ),
              Checkbox(
                value: filterFinished[0], 
                
                fillColor: MaterialStateProperty.all(Colors.white),
                checkColor: Colors.red,
                materialTapTargetSize: MaterialTapTargetSize.padded,
                onChanged: (bool? value){
                  setState(() {
                    filterFinished[0]=value!;
                });
                }

              ),
              const Padding(
                padding: EdgeInsets.fromLTRB(20, 0, 0, 0),
                child: Text("Finished", style:TextStyle(color: Colors.white)),
              ),
              Checkbox(
                value: filterFinished[1], 
                
                fillColor: MaterialStateProperty.all(Colors.white),
                checkColor: Colors.red,
                materialTapTargetSize: MaterialTapTargetSize.padded,
                onChanged: (bool? value){
                  setState(() {
                    filterFinished[1]=value!;
                });
                }

              )
            ],
          
          ),
          for(int i=0;i<types.length;i++)
            
            Row(
              children: [
                Padding(
                  padding: const EdgeInsets.fromLTRB(20, 0, 0, 0),
                  child: Image(image: AssetImage("assets/Icons/${types[i]}.png"),width: 80, height: 80,),
                ),
                Text(types[i],style: const TextStyle(color: Colors.white),),
                
                Column(
                  children: [
                    Checkbox(value: availableTypes.contains(types[i]), 
                      fillColor: MaterialStateProperty.all(Colors.white),
                      checkColor: Colors.red,
                      materialTapTargetSize: MaterialTapTargetSize.padded,
                      onChanged: (bool? value){
                        setState(() {
                          if(!value!)
                          {
                            availableTypes.remove(types[i]);
                          }
                          else
                          {
                            availableTypes.add(types[i]);
                          }
                      });
                      }
                    ),
                  ],
                )
              ],
            ),
        ],
      ),
    );
  }
}

class MyApp extends StatefulWidget {
  const MyApp({super.key});

  @override
  State<MyApp> createState() => _MyAppState();
}

class _MyAppState extends State<MyApp> {
  @override
  Widget build(BuildContext context) {
    return FutureBuilder(
      future: loadProgress(),
      builder: (context, snapshot) {
        if (snapshot.connectionState == ConnectionState.done) {
          return const MaterialApp(
            debugShowCheckedModeBanner: false,
            home: Scaffold(
              endDrawer: NavDrawer(),
              body: Main(),
            ),
          );
        } else {
          return const MaterialApp(
              debugShowCheckedModeBanner: false,
              home: Scaffold(body: CircularProgressIndicator()));
        }
      },
    );
  }
}

